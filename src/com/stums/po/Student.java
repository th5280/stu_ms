package com.stums.po;



public class Student {
    private Integer stu_sid;

    private String stu_nid;

    private String stu_name;

    private Integer stu_major_id;

    private int stu_enter_year;

    private Integer stu_sex;

    private String stu_tel;

    private Integer stu_class;

    private Integer stu_college_id;

	public Integer getStu_sid() {
		return stu_sid;
	}

	public void setStu_sid(Integer stuSid) {
		stu_sid = stuSid;
	}

	public String getStu_nid() {
		return stu_nid;
	}

	public void setStu_nid(String stuNid) {
		stu_nid = stuNid;
	}

	public String getStu_name() {
		return stu_name;
	}

	public void setStu_name(String stuName) {
		stu_name = stuName;
	}

	public Integer getStu_major_id() {
		return stu_major_id;
	}

	public void setStu_major_id(Integer stuMajorId) {
		stu_major_id = stuMajorId;
	}

	public int getStu_enter_year() {
		return stu_enter_year;
	}

	public void setStu_enter_year(int stuEnterYear) {
		stu_enter_year = stuEnterYear;
	}

	public Integer getStu_sex() {
		return stu_sex;
	}

	public void setStu_sex(Integer stuSex) {
		stu_sex = stuSex;
	}

	public String getStu_tel() {
		return stu_tel;
	}

	public void setStu_tel(String stuTel) {
		stu_tel = stuTel;
	}

	public Integer getStu_class() {
		return stu_class;
	}

	public void setStu_class(Integer stuClass) {
		stu_class = stuClass;
	}

	public Integer getStu_college_id() {
		return stu_college_id;
	}

	public void setStu_college_id(Integer stuCollegeId) {
		stu_college_id = stuCollegeId;
	}

   
}