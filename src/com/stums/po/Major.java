package com.stums.po;

public class Major {
    private Integer major_id;

    private String major_name;

    private Integer major_head_id;
    
    private Integer major_college_id;

	public Integer getMajor_id() {
		return major_id;
	}

	public void setMajor_id(Integer majorId) {
		major_id = majorId;
	}

	public String getMajor_name() {
		return major_name;
	}

	public void setMajor_name(String majorName) {
		major_name = majorName;
	}

	public Integer getMajor_head_id() {
		return major_head_id;
	}

	public void setMajor_head_id(Integer majorHeadId) {
		major_head_id = majorHeadId;
	}

	public Integer getMajor_college_id() {
		return major_college_id;
	}

	public void setMajor_college_id(Integer majorCollegeId) {
		major_college_id = majorCollegeId;
	}

    
}