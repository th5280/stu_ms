package com.stums.po;

public class MajorView extends Major{
	
	public String major_head_name;
	public String college_name;
	public String stu_num;
	
	public String getMajor_head_name() {
		return major_head_name;
	}
	public void setMajor_head_name(String majorHeadName) {
		major_head_name = majorHeadName;
	}
	public String getCollege_name() {
		return college_name;
	}
	public void setCollege_name(String collegeName) {
		college_name = collegeName;
	}
	public String getStu_num() {
		return stu_num;
	}
	public void setStu_num(String stuNum) {
		stu_num = stuNum;
	}
	
	
}
